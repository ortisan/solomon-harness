def _instruction_is_valid(
    path: Path, host: str, rules_reference: str
) -> bool:
    text = _read_text(path)
    if text is None or _TEXT_START not in text or _TEXT_END not in text:
        return False
    if host == "claude":
        return f"@../{rules_reference}" in text
    return rules_reference in text


def _overall_status(states: Mapping[str, str]) -> str:
    configured = {state for state in states.values() if state != "unavailable"}
    if "disabled" in configured:
        return "disabled"
    if "pending_trust" in configured:
        return "pending_trust"
    if "configured" in configured:
        return "configured"
    return "active" if configured else "unavailable"
