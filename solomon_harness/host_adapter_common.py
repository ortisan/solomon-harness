def _runtime_project_is_valid(root: Path, runtime: _RuntimeLayout) -> bool:
    """Confirm that a generated MCP command points at an in-repository runtime."""

    project = root if runtime.source_checkout else root / ".agents" / "solomon"
    try:
        project.resolve().relative_to(root.resolve())
    except (OSError, ValueError):
        return False
    required = (
        project / "pyproject.toml",
        project / "solomon_harness" / "mcp_server.py",
    )
    return all(path.is_file() and not path.is_symlink() for path in required)


def _write_adapter_payload(stream: BinaryIO, payload: bytes) -> None:
    """Write and flush one temporary adapter payload before publication."""

    stream.write(payload)
    stream.flush()
    os.fsync(stream.fileno())


def _atomic_adapter_write(root: Path, path: Path, payload: bytes, mode: int) -> None:
    """Publish one deterministic adapter without exposing partial bytes."""

    ensure_install_parent(
        root,
        path,
        private_root=HarnessPaths(root).state,
    )
    descriptor, temporary_name = tempfile.mkstemp(
        prefix=f".{path.name}.",
        dir=path.parent,
    )
    temporary = Path(temporary_name)
    try:
        with os.fdopen(descriptor, "wb") as stream:
            _write_adapter_payload(stream, payload)
        os.chmod(temporary, mode)
        os.replace(temporary, path)
        # Register immediately after the atomic visibility point. If the
        # directory durability flush fails, an install transaction can now
        # compare-and-swap the published bytes back to its snapshot.
        record_install_mutation(path)
        if os.name != "nt":
            directory = os.open(path.parent, os.O_RDONLY)
            try:
                os.fsync(directory)
            finally:
                os.close(directory)
    finally:
        temporary_existed = temporary.exists()
        temporary.unlink(missing_ok=True)
        if temporary_existed:
            record_install_mutation(path.parent)


class _Recorder:
    def __init__(
        self, root: Path, previous_ownership: Mapping[str, _OwnedAdapter]
    ) -> None:
        self.root = root
        self.previous_ownership = previous_ownership
        self.changed = False
        self.conflicts: set[str] = set()
        self.managed: set[str] = set()
        self._source_checkout: Optional[bool] = None

    def relative(self, path: Path) -> str:
        return path.relative_to(self.root).as_posix()

    def conflict(self, path: Path) -> None:
        self.conflicts.add(self.relative(path))

    def mark_managed(self, path: Path) -> None:
        ensure_install_parent(
            self.root,
            path,
            private_root=HarnessPaths(self.root).state,
        )
        previous = self.previous_ownership.get(self.relative(path))
        if previous is not None and not self.mode_unchanged(path):
            self.conflict(path)
            return
        self.managed.add(self.relative(path))

    def mode_unchanged(self, path: Path) -> bool:
        previous = self.previous_ownership.get(self.relative(path))
        try:
            mode = stat.S_IMODE(path.stat().st_mode)
        except OSError:
            return False
        return previous is not None and previous.mode == mode

    def owned_unchanged(self, path: Path) -> bool:
        previous = self.previous_ownership.get(self.relative(path))
        if not previous or not path.is_file() or path.is_symlink():
            return False
        if previous.managed_sha256 is not None:
            try:
                digest = managed_adapter_digest(path, self.relative(path))
            except (OSError, RuntimeError, ValueError):
                return False
        else:
            digest = hashlib.sha256(path.read_bytes()).hexdigest()
        return bool(
            (previous.managed_sha256 or previous.sha256) == digest
            and self.mode_unchanged(path)
        )

    def clean_tracked_source_adapter(self, path: Path) -> bool:
        """Prove ownership of a manifest-free adapter in the source checkout."""

        if not self._is_source_checkout() or not path.is_file() or path.is_symlink():
            return False
        relative = self.relative(path)
        tracked = self._git("ls-files", "--error-unmatch", "--", relative)
        clean = self._git("diff", "--quiet", "HEAD", "--", relative)
        return bool(
            tracked
            and tracked.returncode == 0
            and clean
            and clean.returncode == 0
        )

    def is_managed(self, path: Path) -> bool:
        return bool(
            self.relative(path) in self.previous_ownership
            or self.clean_tracked_source_adapter(path)
        )

    def managed_unchanged(self, path: Path) -> bool:
        if self.relative(path) in self.previous_ownership:
            return self.owned_unchanged(path)
        return self.clean_tracked_source_adapter(path)

    def unsafe(self, path: Path) -> bool:
        """Reject adapter targets that traverse symlinks or leave the project."""

        try:
            path.parent.resolve().relative_to(self.root.resolve())
        except ValueError:
            return True
        cursor = path
        while cursor != self.root:
            if cursor.is_symlink():
                return True
            cursor = cursor.parent
        return False

    def _git(self, *arguments: str, capture: bool = False) -> subprocess.CompletedProcess[str] | None:
        environment = dict(os.environ)
        for key in ("GIT_DIR", "GIT_WORK_TREE", "GIT_INDEX_FILE"):
            environment.pop(key, None)
        try:
            return subprocess.run(  # noqa: S603 - private callers supply fixed Git argv
                ["git", "-C", str(self.root), *arguments],  # noqa: S607 - operator PATH
                check=False,
                env=environment,
                stdin=subprocess.DEVNULL,
                stdout=subprocess.PIPE if capture else subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                text=True,
            )
        except OSError:
            return None

    def _is_source_checkout(self) -> bool:
        """Prove this is the harness source tree, rather than a consumer repo."""

        if self._source_checkout is not None:
            return self._source_checkout
        top_level = self._git("rev-parse", "--show-toplevel", capture=True)
        source_paths = (
            "pyproject.toml",
            "agents/AGENTS.md",
            "solomon_harness/__init__.py",
            "solomon_harness/mcp_server.py",
            "scripts/generate-integrations.py",
        )
        tracked = tuple(
            self._git("ls-files", "--error-unmatch", "--", relative)
            for relative in source_paths
        )
        try:
            git_root = Path(top_level.stdout.strip()).resolve() if top_level else None
            metadata = tomllib.loads(
                (self.root / "pyproject.toml").read_text(encoding="utf-8")
            )
            project = metadata.get("project", {})
            project_name = project.get("name") if isinstance(project, Mapping) else None
        except (OSError, UnicodeDecodeError, tomllib.TOMLDecodeError):
            git_root = None
            project_name = None
        self._source_checkout = bool(
            top_level
            and top_level.returncode == 0
            and git_root == self.root.resolve()
            and project_name == "solomon-harness"
            and all(
                process is not None and process.returncode == 0 for process in tracked
            )
            and all(
                (self.root / relative).is_file()
                and not (self.root / relative).is_symlink()
                for relative in source_paths
            )
        )
        return self._source_checkout

    def clean_tracked_generated_source(self, path: Path, current: bytes) -> bool:
        """Allow a manifest-free upgrade only with independent Git ownership proof."""

        if not self._is_source_checkout():
            return False
        try:
            text = current.decode("utf-8")
        except UnicodeDecodeError:
            return False
        if not any(marker in text for marker in (_GENERATED_MARKER, _LEGACY_GENERATED_MARKER)):
            return False
        return self.clean_tracked_source_adapter(path)

    def write_generated(self, path: Path, content: str) -> None:
        """Write an owned whole-file adapter without clobbering user content."""

        if self.unsafe(path):
            self.conflict(path)
            return
        ensure_install_parent(
            self.root,
            path,
            private_root=HarnessPaths(self.root).state,
        )
        payload = content.encode("utf-8")
        rel = self.relative(path)
        if path.exists():
            current = path.read_bytes()
            if current == payload:
                self.mark_managed(path)
                return
            previous_matches = self.owned_unchanged(path)
            if not previous_matches and not self.clean_tracked_generated_source(
                path, current
            ):
                self.conflict(path)
                return
        _atomic_adapter_write(self.root, path, payload, _ADAPTER_FILE_MODE)
        self.changed = True
        self.managed.add(rel)

    def write_merged(self, path: Path, content: str) -> None:
        """Write a semantic merge that already retained unrelated content."""

        if self.unsafe(path):
            self.conflict(path)
            return
        ensure_install_parent(
            self.root,
            path,
            private_root=HarnessPaths(self.root).state,
        )
        payload = content.encode("utf-8")
        if (
            path.exists()
            and self.relative(path) in self.previous_ownership
            and not self.mode_unchanged(path)
        ):
            self.conflict(path)
            return
        if path.exists() and path.read_bytes() == payload:
            self.mark_managed(path)
            return
        mode = stat.S_IMODE(path.stat().st_mode) if path.exists() else _ADAPTER_FILE_MODE
        _atomic_adapter_write(self.root, path, payload, mode)
        self.changed = True
        self.managed.add(self.relative(path))

    def result(self) -> AdapterCompileResult:
        return AdapterCompileResult(
            changed=self.changed,
            conflicts=tuple(sorted(self.conflicts)),
            managed_paths=tuple(sorted(self.managed)),
        )


def _load_previous_ownership(root: Path) -> dict[str, _OwnedAdapter]:
    manifest = root / ".agents" / "solomon" / "manifest.json"
    if not manifest.exists():
        return {}
    if manifest.is_symlink() or not manifest.is_file():
        raise AdapterOwnershipError(
            f"Install manifest is not a regular file: {manifest}"
        )
    try:
        document = json.loads(manifest.read_text(encoding="utf-8"))
    except (OSError, UnicodeDecodeError, json.JSONDecodeError) as exc:
        raise AdapterOwnershipError(
            f"Cannot read install manifest: {manifest}"
        ) from exc
    if not isinstance(document, Mapping):
        raise AdapterOwnershipError("Install manifest must be a JSON object")
    if document.get("schema_version") != 1:
        raise AdapterOwnershipError("Unsupported install manifest schema")

    result: dict[str, _OwnedAdapter] = {}
    seen: set[str] = set()
    entries = document.get("entries")
    if not isinstance(entries, list):
        raise AdapterOwnershipError("Install manifest entries must be a list")
    for entry in entries:
        if not isinstance(entry, Mapping):
            raise AdapterOwnershipError("Install manifest contains an invalid entry")
        path = entry.get("path")
        owner = entry.get("owner")
        strategy = entry.get("strategy")
        digest = entry.get("sha256")
        mode = entry.get("mode")
        managed_digest = entry.get("managed_sha256")
        if not isinstance(path, str) or not _safe_manifest_path(path):
            raise AdapterOwnershipError(
                f"Install manifest contains an unsafe path: {path!r}"
            )
        if path in seen:
            raise AdapterOwnershipError(
                f"Install manifest contains a duplicate path: {path}"
            )
        seen.add(path)
        if owner not in {"adapter", "core", "project"}:
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid owner for {path}"
            )
        if strategy not in {
            "replace",
            "create-only",
            "json-merge",
            "toml-merge",
            "marker-merge",
        }:
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid strategy for {path}"
            )
        if not isinstance(digest, str) or re.fullmatch(
            r"[0-9a-f]{64}", digest
        ) is None:
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid digest for {path}"
            )
        if not isinstance(mode, str) or re.fullmatch(r"0[0-7]{3}", mode) is None:
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid mode for {path}"
            )
        if "created" in entry and not isinstance(entry["created"], bool):
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid creation marker for {path}"
            )
        base_digest = entry.get("base_sha256")
        if base_digest is not None and (
            not isinstance(base_digest, str)
            or re.fullmatch(r"[0-9a-f]{64}", base_digest) is None
        ):
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid base digest for {path}"
            )
        if managed_digest is not None and (
            owner != "adapter"
            or strategy not in {"json-merge", "toml-merge", "marker-merge"}
            or not isinstance(managed_digest, str)
            or re.fullmatch(r"[0-9a-f]{64}", managed_digest) is None
        ):
            raise AdapterOwnershipError(
                f"Install manifest contains an invalid managed digest for {path}"
            )
        if owner == "adapter":
            if not _adapter_manifest_path_allowed(path):
                raise AdapterOwnershipError(
                    f"Install manifest adapter path is outside its namespace: {path}"
                )
            if strategy != strategy_for_adapter(path):
                raise AdapterOwnershipError(
                    f"Install manifest adapter strategy does not match {path}"
                )
            result[path] = _OwnedAdapter(digest, int(mode, 8), managed_digest)
        elif owner == "core":
            if not path.startswith(".agents/solomon/") or path in {
                ".agents/solomon/config/project.json",
                ".agents/solomon/manifest.json",
            }:
                raise AdapterOwnershipError(
                    f"Install manifest core path is outside its namespace: {path}"
                )
            if strategy not in {"replace", "create-only"}:
                raise AdapterOwnershipError(
                    f"Install manifest core strategy does not match {path}"
                )
        else:
            if not _project_manifest_path_allowed(path):
                raise AdapterOwnershipError(
                    f"Install manifest project path is outside its namespace: {path}"
                )
            if strategy != "create-only":
                raise AdapterOwnershipError(
                    f"Install manifest project strategy does not match {path}"
                )
    return result


def _safe_manifest_path(relative: str) -> bool:
    path = Path(relative)
    return bool(
        relative
        and not path.is_absolute()
        and path.as_posix() == relative
        and "." not in path.parts
        and ".." not in path.parts
    )


def _adapter_manifest_path_allowed(relative: str) -> bool:
    fixed = {
        ".agents/hooks.json",
        ".agents/mcp_config.json",
        ".agents/plugins/solomon/mcp_config.json",
        ".agents/plugins/solomon/plugin.json",
        ".claude/CLAUDE.md",
        ".claude/settings.json",
        ".codex/config.toml",
        ".codex/hooks.json",
        ".mcp.json",
        "AGENTS.md",
    }
    if relative in fixed:
        return True
    parts = Path(relative).parts
    return bool(
        (
            len(parts) == 4
            and parts[:2] == (".agents", "agents")
            and parts[2]
            and parts[3] == "agent.md"
        )
        or (
            len(parts) == 4
            and parts[:2] == (".agents", "skills")
            and parts[2].startswith("solomon-")
            and parts[3] == "SKILL.md"
        )
        or (
            len(parts) == 3
            and parts[:2] == (".claude", "agents")
            and parts[2].endswith(".md")
        )
        or (
            len(parts) == 4
            and parts[:2] == (".claude", "skills")
            and parts[2].startswith("solomon-")
            and parts[3] == "SKILL.md"
        )
        or (
            len(parts) == 3
            and parts[:2] == (".codex", "agents")
            and parts[2].endswith(".toml")
        )
    )


def _project_manifest_path_allowed(relative: str) -> bool:
    if relative == ".agents/solomon/config/project.json":
        return True
    if relative in {
        ".github/PULL_REQUEST_TEMPLATE.md",
        "docs/adrs/0000-adr-template.md",
        "docs/adrs/README.md",
        "docs/specs/0000-spec-template.md",
        "docs/specs/README.md",
    }:
        return True
    parts = Path(relative).parts
    return len(parts) == 3 and parts[:2] == (".github", "ISSUE_TEMPLATE")


