"""Compile a neutral harness catalog into thin native host adapters.

Consumer repositories read the catalog below ``.agents/solomon``. A Git-proven
harness source checkout reads its tracked source catalog in place. Everything
rendered here contains only native metadata, configuration, and pointers back
to the selected catalog. This keeps host vocabulary out of workflow and
specialist definitions and makes three-host parity deterministic.
"""

